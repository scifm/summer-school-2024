# ‚ïî‚ïê‚ï° 038283a6-8f58-4029-9d0b-01614741e479
md"""
## Limits of Scaling
Unfortunatly, scale isn't everything. Langauge has an intrinsic entropy, beyond which futher improvement isn't possible. [Shun-ichi Amari and Noboru Murata](https://doi.org/10.1162/neco.1993.5.1.140) provide an analytical derivation for this limit assuming both a asymptotically corrent and incorrect models.

``
H = \sum_{x \in \chi} p(x)\log p(x)
``

For example, english has a Shannon Entropy (H) of ~2.6 bits/character, with the caveat that the proability of each character is independent the other character.

In [Hoffmann et. al](https://arxiv.org/abs/2203.15556) this is modeled by ``E`` which represents the minimum possible loss in the limit of infinite data, model parameters and compute. [Kaplan et. al](https://doi.org/10.48550/arXiv.2001.08361) also noted that the lost "must flatten out evenutally before reaching zero," but did not observe it.

``
\lim\limits_{N,D \to \infty} L(N, D) \to E
``

On the plus side, this limit tends to be more theroetical than practical. Plugging in ``N_{opt}`` and ``D_{opt}`` into ``L``, we can estimate the loss of a compute-optimal model for a given budget.
"""

# ‚ïî‚ïê‚ï° 32952bfd-3751-404f-8e75-c34541402ed0
function compute_optimal_loss(C; p)
	(; A, Œ±, B, Œ≤, E) = p
    G = ((Œ± * A) / (Œ≤ * B))^(1 / (Œ± + Œ≤))
    a = Œ≤ / (Œ± + Œ≤)
    b = Œ± / (Œ± + Œ≤)
    d = C / 6
    return E + A * (G * d^a)^(-Œ±) + B * (inv(G) * d^b)^(-Œ≤)
end

# ‚ïî‚ïê‚ï° ead24db3-9b33-4d51-9020-2a0fef111c71
begin
function plot_compute_optimal_loss(C, loss, p)
	C_fit = logrange(1e15, 1e30; length=100)
	loss_fit = map(c -> compute_optimal_loss(c; p), C_fit)
	f = Figure()
	ax = Axis(f[1, 1];
		limits=(extrema(C_fit), (1, nothing)),
		xscale=log10, yscale=log10,
		xlabel="Compute Budget [FLOPS]",
		ylabel="Loss",
	)
	lines!(ax, C_fit, loss_fit; color=:red, label="Compute-Optimal Loss")
	scatter!(ax, C, loss; label="Emperical")
	hlines!(ax, p.E; label="Fitted Minimal Loss", color=:black)
	axislegend(ax)
	
	return f
end

plot_compute_optimal_loss(
	@.(6 * p.data_size * p.model_size),
	p.loss,
	p_hoffmann,
)
end

