# ‚ïî‚ïê‚ï° ecde9ae9-7daf-4ccc-bb4c-9848fff9a4db
md"""
# Fitting Hoffmann et. al.'s Scaling Model
We're going to fit the scaling law proposed by [Hoffmann et al.](https://arxiv.org/abs/2203.15556), which extends [Kaplan et al.](https://arxiv.org/abs/2001.08361) by adding the ``E`` term:

``
L(N, D) = \frac{A}{N^{\alpha}} + \frac{B}{D^{\beta}} + E
``

| Parameter | Meaning | Type |
|:-----------:|:-------|:--------:|
| ``N``   	| Number of *non-embedding* parameters | Covariate |
| ``D`` 	| Number of tokens seen by the model during training | Covariate |
| ``L``     | Loss of the model for ``N`` and ``D`` | Response |
| ``A``, ``\alpha`` | Scaling parameters for model size | Parameter |
| ``B``, ``\beta`` | Scaling parameters for data size | Parameter |
| ``E`` | Intrinsic Entropy of the Data | Parameter |

We're going to follow the procedure used by [Hoffman et al.](https://arxiv.org/abs/2203.15556) to fit the above function. Specfically, we're going to minimize the following:

``
\min\limits_{a, b, e, \alpha, \beta} \sum_i Huber_{\delta} \left(
	LSE(a - \alpha \log N_i, b - \beta \log D_i, e) - \log L_i
\right)
``

To avoid ``\log(-1)`` errors, we do the following transformation: ``A = \exp(a)``, ``B = \exp(b)`` and ``E = \exp(e)``.

The actual optimization, uses the L-BFGS algorithm from [Optim.jl](https://julianlsolvers.github.io/Optim.jl/stable/) using [Optimization.jl](https://docs.sciml.ai/Optimization/stable/) with forward-mode automatic differentiation via [ForwardDiff.jl](https://github.com/JuliaDiff/ForwardDiff.jl).
"""

# ‚ïî‚ïê‚ï° 563bc8bf-23b8-40d5-85f2-782794df3a54
begin
"""
	l = lse(x...)

Computes the [log-sum-exp](https://en.wikipedia.org/wiki/LogSumExp) of it's inputs
"""
lse(x...) = log(sum(exp, [x...]))

"""
	l = hoffmann_scaling(x, p)

Computes the Huber loss for the parameters `x = (Œ±, Œ≤, ...)` and data
`p = (; model_size, data_size loss)` following the procedure outlined
by [Hoffman et al.](https://arxiv.org/abs/2203.15556)
"""
function hoffmann_scaling(x, p)
	# Unpack the data
	(; model_size, data_size, loss) = p
	Œ±, Œ≤, a, b, e = x

	# Compute Huber Loss over the data
	fit_loss = 0
	lossfn = HuberLoss(1e-3) # Set Œ¥=1e-3, per Hoffman et. al.
	for (N, D, l) in zip(model_size, data_size, loss)
		loss_lse = lse(a - Œ± * log(N), b - Œ≤ * log(D), e)
		fit_loss += lossfn(loss_lse - log(l))
	end
	return fit_loss
end

end;

# ‚ïî‚ïê‚ï° 59c6227a-b656-4d6d-8ffb-295d9672a75f
md"""
## Initial Grid Search
To avoid getting stuck in a local minima, we're first going to perform a grid search over "resonable" values for ``\alpha``, ``\beta``, a, b & e.
"""

# ‚ïî‚ïê‚ï° a2219f82-ea42-4964-b2e9-fb3f9be3846c
grid = Iterators.product(
	0:0.25:2, 	# Œ±
	0:0.25:2, 	# Œ≤
	0:5:10,  	# a = log(A)
	0:5:10, 	# b = log(B)
	-1:0.25:1, 	# e = log(E)
)

# ‚ïî‚ïê‚ï° 609bccd9-438b-4490-ab67-40d0c3a1d189
md"""
We're going to pretend training stopped after the first epoch, but in reality [Muennighoff et. al.'s](https://arxiv.org/abs/2305.16264) trained for multiple epochs. To fit on the total number of tokens the model saw change `df.unique_tokens` => `df.total_tokens` (The notebook will update automatically)
"""

# ‚ïî‚ïê‚ï° c4e03d21-1bbb-4093-93b5-2f66b275102a
p = (; model_size=df.model_size, data_size=df.unique_tokens, loss=df.loss);

# ‚ïî‚ïê‚ï° 309bd25c-d20a-4a08-bd84-620d9a91f0b8
# Not sure what a function does? Click on it, then on "Live Docs" in the lower right
x0 = argmin(grid) do x
	hoffmann_scaling(x, p)
end |> x -> [x...] # This splats out x (a tuple) into a vector

# ‚ïî‚ïê‚ï° 324b7bf4-31cf-4376-bccb-5e17a8f72b78
md"""
## Fitting Parameters with L-BFGS
We're using [Optimization.jl](https://docs.sciml.ai/Optimization) from Julia's [SciML](https://sciml.ai/) ecosystem, so the setting up and fitting the coefficients is pretty simple (and super customizable). For a deeper dive, see [Solving the Rosenbrock Problem in >10 Ways](https://docs.sciml.ai/Optimization/stable/examples/rosenbrock/).

Afterwards, we're left with our fitted parameters:
"""

# ‚ïî‚ïê‚ï° e16651d2-be77-4399-ad84-8bc722183be4
begin
	prob_hoffmann = OptimizationProblem(
		OptimizationFunction(hoffmann_scaling, AutoForwardDiff()),
		x0, p,
	)
	sol_hoffmann = solve(prob_hoffmann, Optim.LBFGS())
	p_hoffmann = (;
		Œ±=sol_hoffmann.u[1],
		Œ≤=sol_hoffmann.u[2],
		A=exp(sol_hoffmann.u[3]),
		B=exp(sol_hoffmann.u[4]),
		E=exp(sol_hoffmann.u[5]),
	)
end

# ‚ïî‚ïê‚ï° bcd3d39f-67d4-4622-82a6-912c7cab6161
md"""
# Training Compute Optimal Models
Now that we have fits for A, ``\alpha``, B, ``\beta`` and E, we can estimate the optimal model size for a given compute budget.

We're going to skip the derivation (See [Hoffmann et al.](https://arxiv.org/abs/2203.15556)); but the puch line are scaling laws for model and data size with the compute budget:

`` N_{opt}(C) = G \left( \frac{C}{6} \right)^a ``

`` D_{opt}(C) = G^{-1} \left( \frac{C}{6} \right)^b ``

Where:

``G = \left( \frac{\alpha A}{\beta B} \right)^{\frac{1}{\alpha+\beta}}``

``a = \frac{\beta}{\alpha + \beta} ``

``b = \frac{\alpha}{\alpha + \beta} ``

Interestingly, [Hoffmann et al.](https://arxiv.org/abs/2203.15556) found that ``a \approx b``, impling that the number of parameters and number of tokens should be scaled evenly. That is, when doubling the number of parameters the number of tokens should also be doubled. Conversely, [Kaplan et al.](https://arxiv.org/abs/2001.08361) found that model size sould scale faster with ``N^{0.74}/D``.

| Source | ``a`` | ``b`` |
|--------|-------|-------|
| [Hoffmann et al.](https://arxiv.org/abs/2203.15556) | 0.50 | 0.50 |
| [Kaplan et al.](https://arxiv.org/abs/2001.08361) | 0.73 | 0.27 |
| Us üéâ | $(round(p_hoffmann.Œ≤ / (p_hoffmann.Œ± + p_hoffmann.Œ≤); digits=2)) | $(round(p_hoffmann.Œ± / (p_hoffmann.Œ± + p_hoffmann.Œ≤); digits=2)) |
"""

# ‚ïî‚ïê‚ï° b02dd2d6-4206-43ce-867e-db8b8e6ae616
"""
	N_opt = compute_optimal_model(C; p)

Returns the optimal model size for a given compute budget (C) in FLOPs
"""
function compute_optimal_model(C; p)
	(; A, Œ±, B, Œ≤, E) = p
	G = ((Œ± * A) / (Œ≤ * B)) ^ (1/(Œ± + Œ≤))
	a = Œ≤ / (Œ± + Œ≤) # Yes, a was log(A) during fitting üôÉ
	return G * (C/6)^a
end;

# ‚ïî‚ïê‚ï° 86466dc1-356d-4c85-94e4-4b68aa29fceb
md"""
## Estimating Compute Costs
How much compute does it take to train a model with ``N`` parameters on ``D`` tokens?
If we define "compute" as the number of flops to train the model, and not the "wall-time" or "node-hours" needed, we can tabulate the costs directly:

Roughly, during the forward pass each parameter is involved in 2 floating-point operations: one multiply and one accumulate. Giving a total cost of ``\approx 2N`` for the forward pass. The backward pass costs ~2x the forward pass (update parameters and optimizer states), giving a total cost of ``C \approx 6ND``.

For more details and a per component tabuluation see [Kaplan et. al.](https://doi.org/10.48550/arXiv.2001.08361)
"""

